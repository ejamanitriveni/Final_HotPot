export class Customer {
    id:number;
    name:string;
    email:string;
    phone:string;
    userName:string;

    constructor(
        id:number,
        name:string,
        email:string,
        phone:string,
        userName:string
        ){
            this.id=id;
            this.name=name;
            this.email=email;
            this.phone=phone;
            this.userName=userName;
        }


}
